package JavaBasics;

public class LoopsConcept {

	public static void main(String[] args) {
		//1. while loop
		
		int i =1;
		while(i<=10) {
			
			System.out.println(i);
			
			i=i+1;
			
		}
		
		System.out.println("**********");
	
	//2. for loop
	
	for(int j=1;j<=10;j++) {
	 
	  System.out.println(j);
		
	 }
	
   }
}
